//隐式类型转换
#include<stdio.h>
int main(void){
    // 1 int   0.9 double
    // 整转浮
    printf("%lu\n",sizeof(1 ? 1 : 0.9));
    // 3u unsigned int
    // -7 int
    // 7  00000000 00000000 00000000 00000111
    // ~  11111111 11111111 11111111 11111000
    // +1 11111111 11111111 11111111 11111001
    // 3  00000000 00000000 00000000 00000011-7 + 3u > 0
    // --------------------------------------
    //    11111111 11111111 11111111 11111100  负数
    //    00000000 00000000 00000000 00000011
    //    00000000 00000000 00000000 00000100   4
    // 有转无
    printf("%d\n",-7 + 3u > 0);
    printf("%d\n",-7 + 3u);
    int a = 300;
    char c = (char)a;
    printf("c = %d\n",c);
    return 0;
}
